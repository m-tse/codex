# Installing homebrew
/bin/bash -c "$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/master/install.sh)"

brew cask install visual-studio-code
brew cask install google-backup-and-sync
# To get window management keyboard shortcuts
brew cask install rectangle
# To get mouse 4/5 to behave as back/forward
brew cask install sensiblesidebuttons

# Command line tools
# Fancy cat with syntax highlighting and line numbers
brew install bat
# View directory tree structure
brew install tree
# Fancy fast find
brew install fd
# Fancy fast grep
brew install rg
brew install tmux
# Fuzzy command line finder
brew install fzf
